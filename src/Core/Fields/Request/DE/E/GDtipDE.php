  public GCamAE $gCamAE;
  public GCamNCDE $gCamNCDE;
  public GCamNRE $gCamNRE;
  public GCamCond $gCamCond;
  ////ARRAY DE GCAMITEM
  public  array $gCamItem;
  public GCamEsp $gCamEsp;

  //====================================================//
  ///Others
